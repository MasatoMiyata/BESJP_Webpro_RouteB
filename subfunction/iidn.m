%　コメント 通算日計算
%-----------------------------------------------------------------------------------
%　入力  month:月,day:日　
%-----------------------------------------------------------------------------------
%　出力  IIDN:通算日
%-----------------------------------------------------------------------------------
function IIDN = iidn(month,day)

IIDN = fix( 30*(month-1) + fix((month+fix(month/8))/2) - fix((month+7)/10) + day);